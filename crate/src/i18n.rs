pub fn t(lang: &str, key: &str) -> String {
    let table = match lang {
        "es" => es(),
        _ => en(),
    };
    table.strings
        .get(key)
        .copied()
        .or_else(|| en().strings.get(key).copied())
        .unwrap_or(key)
        .to_string()
}
